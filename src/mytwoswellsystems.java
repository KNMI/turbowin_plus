   mywaves.Pw1_code                   = "";
   mywaves.Hw1_code                   = "";
   mywaves.Dw2_code                   = "";
   mywaves.Pw2_code                   = "";
   mywaves.Hw2_code                   = "";
      
   // local var's with initialisation
   float_swell_1_height               = main.INVALID;               // could become e.g. 2.5 (-> use float)
   float_swell_2_height               = main.INVALID;               // could become e.g. 2.5 (-> use float)
