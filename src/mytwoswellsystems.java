private void initSynopparameters()
{
   // put back earlier inserted values (but not 'confused' which could be inserted before on the confused swell input page and 'no swell')
      
   // 1st swell system
   if ((mywaves.swell_1_dir.compareTo("") != 0) && (mywaves.swell_1_dir.compareTo("confused") != 0) && (mywaves.swell_1_dir.compareTo("no swell") != 0) )
      jTextField1.setText(mywaves.swell_1_dir); 
      
   if ((mywaves.swell_1_period.compareTo("") != 0) && (mywaves.swell_1_period.compareTo("confused") != 0)  && (mywaves.swell_1_period.compareTo("no swell") != 0) )
      jTextField2.setText(mywaves.swell_1_period); 
    
   if ((mywaves.swell_1_height.compareTo("") != 0) && (mywaves.swell_1_height.compareTo("confused") != 0)  && (mywaves.swell_1_height.compareTo("no swell") != 0) )
      jTextField3.setText(mywaves.swell_1_height); 
     
   // 2nd swell system
   if (mywaves.swell_2_dir.compareTo("") != 0)        // 'confused'/'no swell not possible even not via confused swell input screen'/main swell input screen (no swell radiobutton)
      jTextField4.setText(mywaves.swell_2_dir); 
      
   if (mywaves.swell_2_period.compareTo("") != 0)
      jTextField5.setText(mywaves.swell_2_period); 
    
   if (mywaves.swell_2_height.compareTo("") != 0)
      jTextField6.setText(mywaves.swell_2_height); 
}        




