   private void OK_button_actionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_OK_button_actionPerformed
      // TODO add your handling code here:
      boolean doorgaan;
      
      
      ////// obs format (FM13 or 101 or AWS) /////
      //
      if (jRadioButton1.isSelected() == true)         // FM13
      {
         main.obs_format = main.FORMAT_FM13; 
         main.obs_101_encryption = "";
         main.obs_101_email      = "";

         doorgaan = true;
      }
      else if (jRadioButton2.isSelected() == true)    // 101 format
      {
         main.obs_format = main.FORMAT_101;
         doorgaan = true;
      }  
      else if (jRadioButton7.isSelected() == true)    // AWS format
      {
         main.obs_format = main.FORMAT_AWS;
         main.obs_101_encryption = "";
         main.obs_101_email      = "";

         doorgaan = true;
      }        
      else // obs format unknown
      {
         main.obs_format = "";
         doorgaan = false;
         
         String info = "Obs format unknown";
         JOptionPane.showMessageDialog(null, info, main.APPLICATION_NAME + " error", JOptionPane.WARNING_MESSAGE);
      } // else (obs format unknown)
      
      
      
      // obs format 101 (so we have to check also call sign encryption and email body or attachement)
      //
      if ( (doorgaan == true) && (main.obs_format.equals(main.FORMAT_101)) )
      {
         /////// format 101 call sign encryption (yes or no)
         //
         if (jRadioButton3.isSelected() == true)                 // call sign encryption yes
         {
            main.obs_101_encryption = main.FORMAT_101_ENCRYPTION_YES;
            doorgaan = true;
         }
         else if (jRadioButton4.isSelected() == true)            // call sign encryption no
         {
            main.obs_101_encryption = main.FORMAT_101_ENCRYPTION_NO;
            doorgaan = true;
         }
         else
         {
            main.obs_101_encryption = "";    
            doorgaan = false;
         } // else
      
         
         if (doorgaan == true)
         {
            /////// format 101 in email (attachement or body)
            //
            if (jRadioButton5.isSelected() == true)                      // email 101 format in body
            {
               main.obs_101_email =  main.FORMAT_101_BODY;
               doorgaan = true;
            }
            else if (jRadioButton6.isSelected() == true)                 // email 101 format in attachement
            {
               main.obs_101_email =  main.FORMAT_101_ATTACHEMENT;
               doorgaan = true;
            }
            else
            {
               main.obs_101_email = "";
               doorgaan = false;
            }
         } // if (doorgaan == true)
         
         
         // obs format = format 101 AND call sign encryption not ok AND/OR email body/attachment not ok
         //
         if (!(doorgaan == true))
         {
            String info = "\"format 101 call sign encryption\" and/or \"format 101 in E-mail\" unknown";
            JOptionPane.showMessageDialog(null, info, main.APPLICATION_NAME + " error", JOptionPane.WARNING_MESSAGE);
         }
         else // obs format = format 101 AND call sign encryption ok AND email body/attachment ok
         {
            // only Windows virus scanners tend to warn for the compression and decompression programs
            main.OSType ostype = main.detect_OS();
            switch (ostype)
            {
               case WINDOWS: FORMAT_101.compression_exe   = FORMAT_101.COMPRESSION_EXE_WIN; 
                             FORMAT_101.decompression_exe = FORMAT_101.DECOMPRESSION_EXE_WIN;
                             
                             // message (because it could be that virus scanner is complaining about compression exe (teste_hc_TW.exe))
                             String info_message = "In case you get a warning of your virus scanner: " + "\"" + FORMAT_101.compression_exe + "\"" + " and " + "\"" + FORMAT_101.decompression_exe + "\"" + " are essential parts of " + main.APPLICATION_NAME + " required for coding and decoding of format 101";
                             JOptionPane.showMessageDialog(null, info_message, main.APPLICATION_NAME + " info", JOptionPane.INFORMATION_MESSAGE);
                             break;
            } // switch (ostype)  
         } // else       
         
      } // if ( (doorgaan == true) && (main.obs_format.equals(main.FORMAT_101)) )
      
      
      //
      /////// write new (obs) configuration settings to muffin or file(s)
      //
      if (doorgaan == true)
      {
         // write meta (station) data to muffins or configuration files
         if (main.offline_mode_via_cmd == true)
         {
            main.schrijf_configuratie_regels();          
         }
         else // so offline_via_jnlp mode or online (webstart) mode
         {
            main.set_muffin();
            main.schrijf_configuratie_regels(); 
         }   

         /* message */
         String info = "Changes will take full effect after a TurboWin+ restart";
         JOptionPane.showMessageDialog(null, info, main.APPLICATION_NAME + " info", JOptionPane.INFORMATION_MESSAGE);
         
         /* close this obs format settings input page */
         setVisible(false);
         dispose();    
         
      } // if (doorgaan == true)

   }//GEN-LAST:event_OK_button_actionPerformed

   
   
